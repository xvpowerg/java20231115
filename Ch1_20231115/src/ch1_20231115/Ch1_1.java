public class Ch1_1 {

    public static void main(String[] args) {
        //基本
        //整數
            //byte 8bit
            //short 16bit
            //int 32bit
            //long 64bit
        //浮點數
           //float 32bit
           //double 64bit
        //字元
          //char 16bit 無負數
        //布林
          //boolean 1bit
        byte b1 = 10;
        short s1 = 20;
        int myInt = 12345;
        long myLong = 2147483648L;
        System.out.println(b1);
        System.out.println(s1);
        System.out.println(myInt);
        System.out.println(myLong); 
        //字元不可負數
        //字典序
        //數字 < 大寫 < 小寫
        char c1 = 65;//0~65535
        System.out.println(c1);
        boolean bool = true; 
        System.out.println(bool);
        //參考
        String name = "Ken";
        System.out.println(name);
    }
    
}
